/** Получить элемент по slug */
export function getPressItemBySlug(slug: string): PressItem | undefined {
  return PRESS_ITEMS.find((item) => item.slug === slug);
}

/** Получить все элементы определённого типа */
export function getPressItemsByType(type: PressItem["type"]): PressItem[] {
  return PRESS_ITEMS.filter((item) => item.type === type).sort(
    (a, b) => new Date(b.date).getTime() - new Date(a.date).getTime()
  );
}

/** Все slugs (для generateStaticParams) */
export function getAllPressSlugs(): string[] {
  return PRESS_ITEMS.map((item) => item.slug);
}
