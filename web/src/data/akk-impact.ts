// ===== «АКК в масштабе страны» — факты для бегущей ленты ==============
// Горизонтальная авто-лента достижений (ImpactMarquee) сразу под hero:
// карточки-факты сами проезжают справа налево, пауза при наведении.
// Значения и вид визуала — здесь (одна точка правки), тексты — в i18n
// (funnel.impact.items.<key>). Источник цифр — презентация ПП по программе
// «Кең дала» (валовой сбор × доля АКК) и данные АКК за период.
// ВНИМАНИЕ: значения ЧЕРНОВЫЕ — TODO подтвердить у бизнеса перед публичным релизом.
//
// Виды карточек:
//  - 'pictogram' — 3 единицы в ряд, центральная подсвечена «цветом продукта»
//    (изотайп «каждый 3-й …»); крупно выводится процент. icon: 'meat' | 'oil'.
//  - 'counter'   — крупное число `count` (отсчитывается вверх при появлении
//    ленты) + единица из i18n; визуал-глиф зависит от icon.
// =====================================================

export type ImpactIconKey = 'meat' | 'oil' | 'field' | 'people' | 'bread' | 'coin';

export type ImpactFact =
  | {
      key: string;
      kind: 'pictogram';
      icon: 'meat' | 'oil';
      /** Всего единиц в ряду. */
      total: number;
      /** Сколько центральных единиц подсвечено как «доля АКК». */
      filled: number;
      /** Крупный процент (число, без знака «%»). */
      percent: number;
    }
  | {
      key: string;
      kind: 'counter';
      icon: ImpactIconKey;
      /** Число, отсчитывается вверх при появлении ленты. */
      count: number;
      /** Знаков после запятой при анимации (для «4,4»). */
      decimals?: number;
    };

