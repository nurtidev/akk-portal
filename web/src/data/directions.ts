// --- Статья затрат (структура финансирования проекта) ---
export interface CostItem {
  key: string;
  label: string;
  /** Норматив стоимости на 1 единицу мощности (₸, базовый 2024). null — если fixed или нет данных. */
  perUnit: number | null;
  /** Фиксированная стоимость, не зависящая от мощности (₸) — напр. техника, база завода. */
  fixed?: number;
  /** Позиция опциональна (по выбору заёмщика / условию проекта). */
  optional?: boolean;
  note?: string;
}

export type DataStatus = 'full' | 'partial';

export interface Direction {
  id: string;
  group: DirectionGroup;
  title: string;
  icon: string;
  /** Единица измерения мощности (голов, га, тонн, тонн/год, тонн/сутки). */
  capacityUnit: string;
  /** Подпись поля мощности из файла (Main). */
  capacityLabel: string;
  /** Минимальная мощность (текст из требований), если задана. */
  minCapacity?: string;
  /** full — нормативы есть, калькулятор считается; partial — только требования (нет формулы в файле). */
  dataStatus: DataStatus;
  /** Статьи структуры финансирования с нормативами. */
  costItems: CostItem[];
  /** Требования к проекту / биоактивам / объектам (дословно из Main). */
  requirements: RequirementGroup[];
}

