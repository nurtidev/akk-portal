/** Ключи вопросов квиза, по которым строятся hard/soft-правила. */
export type QuestionKey =
  | 'purpose'
  | 'sector'
  | 'experience'
  | 'location'
  | 'amount'
  | 'animalType'
  | 'cattleOrigin'
  | 'heads';

/** Тип графика погашения. */
export type ScheduleType = 'biannual_winter' | 'annual';

/** Жёсткое правило отбора: ответ на вопрос `q` должен быть в списке `v` (AND по правилам). */
export interface HardRule {
  q: QuestionKey;
  v: string[];
}

/** Мягкое правило (вес): если ответ на `q` входит в `v` — прибавить `w` к score. */
export interface SoftRule {
  q: QuestionKey;
  v: string[];
  w: number;
}

/** Разные потолки срока (мес.) в зависимости от цели (`purpose`). */
export type TermByPurpose = Partial<Record<string, number>>;

export interface Program {
  id: string;
  title: string;
  category: string;
  org: string;
  description: string;
  /** Числовая ставка для калькулятора. */
  rate: number;
  /** (опц.) Текстовый диапазон ставки для отображения. Не схлопывать с rate. */
  rateRange?: string;
  /** (опц.) Подсказка-тултип для отображаемой ставки. */
  rateTip?: string;
  /** (опц.) Пояснение про источники/каналы/ставки. */
  rateNote?: string;
  maxAmount: number;
  maxTerm: number;
  /** (опц.) Потолки срока по цели. */
  termByPurpose?: TermByPurpose;
  scheduleType: ScheduleType;
  /** (опц.) Пояснение про график погашения. */
  scheduleNote?: string;
  hard: HardRule[];
  soft: SoftRule[];
  /** (опц.) «Самая популярная» — флаг для сетки. */
  featured?: boolean;
  /** (опц.) Включает шаг Fajr-lite (стресс-тест). */
  hasStressTest?: boolean;
  /** (опц.) Недоступна напрямую — только через КТ/МФО/БВУ; не в результатах квиза. */
  indirectOnly?: boolean;
  /** (опц.) Скрыта из превью-сетки. */
  hidden?: boolean;
}

